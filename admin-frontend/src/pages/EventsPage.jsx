function normalizeLocalized(value) {
  if (!value || typeof value !== 'object') {
    return { ...EMPTY_LOCALIZED, ua: value || '' };
  }

  return {
    ua: value.ua || value.uk || value.ru || value.en || '',
    ru: value.ru || '',
    en: value.en || ''
  };
}

function toDateTimeLocal(value) {
  if (!value) return '';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return '';
  const kyivStr = date.toLocaleString('sv-SE', { timeZone: 'Europe/Kyiv' });
  return kyivStr.slice(0, 16);
}

function fromDateTimeLocal(value) {
  if (!value) return null;
  const parts = String(value).split('T');
  if (parts.length !== 2) return null;
  const [datePart, timePart] = parts;
  const [year, month, day] = datePart.split('-').map(Number);
  const [hour, minute] = timePart.split(':').map(Number);
  const wallClockUtc = Date.UTC(year, month - 1, day, hour, minute);
  const getKyivOffset = (date) => {
    const values = Object.fromEntries(new Intl.DateTimeFormat('en-CA', {
      timeZone: 'Europe/Kyiv',
      year: 'numeric', month: '2-digit', day: '2-digit',
      hour: '2-digit', minute: '2-digit', second: '2-digit', hourCycle: 'h23'
    }).formatToParts(date).map((part) => [part.type, part.value]));
    return Date.UTC(
      Number(values.year), Number(values.month) - 1, Number(values.day),
      Number(values.hour), Number(values.minute), Number(values.second)
    ) - Math.floor(date.getTime() / 1000) * 1000;
  };
  let utcDate = new Date(wallClockUtc - getKyivOffset(new Date(wallClockUtc)));
  utcDate = new Date(wallClockUtc - getKyivOffset(utcDate));
  return Number.isNaN(utcDate.getTime()) ? null : utcDate.toISOString();
}

function getLocaleCode(locale) {
  return locale.toUpperCase();
}

